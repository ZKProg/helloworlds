;; Hello World from scratch.

.define VDPControl  $bf
.define VDPData		$be
.define VRAMWrite	$4000
.define CRAMWrite	$c000

;;-----------------------------------------------------
;; WLA DX INSTRUCTIONS
;;-----------------------------------------------------

.MEMORYMAP
defaultslot 0
slotsize $8000
slot 0 $0000
.ENDME

.ROMBANKMAP
bankstotal 1
banksize $8000
banks 1
.ENDRO

;;-------------------------------------------------------
;; SDSCTAG
;;-------------------------------------------------------

.sdsctag 0.1, "Hello", "A simple hello world.", "ZKProg"

;;-------------------------------------------------------
;; PROGRAM
;;-------------------------------------------------------

.bank 0 slot 0
.org $0000

boot:
	di
	im 1
	jp main
	
;; Pause interrupt
.org $0066
	;; Do nothing
	retn
	
main:
	ld sp, $dff0
	
	;; Setup the VDP
	ld hl, VdpData
	ld b, VdpDataEnd - VdpData
	ld c, VDPControl
	otir 		; <- Only for transfers lower than 256 bytes (b register)
	
	;; Clear the VRAM. We want to write at address 0
	;; Write + address 0 = $4000
	ld a, $00
	out (VDPControl), a
	ld a, $40
	out (VDPControl), a
	;; Output 16k of 0s
	ld bc, $4000 	; $4000 = 16k
	ClearVram:
		ld a, $00
		out (VDPData), a
		dec bc
		ld a, b
		or c
		jp nz, ClearVram
		
	;; Define the palette
	ld a, $00
	out (VDPControl), a
	ld a, $c0		; High byte must be %1100 0000 => Hence $c000 total
	out (VDPControl), a
	;; Block transfer to the color RAM (CRAM)
	ld hl, Palette
	ld b, (PaletteEnd - Palette)
	ld c, VDPData
	otir
	
	;;----------------------------------------------------------
	;; TILES DATA
	;;----------------------------------------------------------
	;; Transfer the tiles
	;; 1- We write at address $00 => $4000 to transfer to $bf port.
	ld a, $00
	out (VDPControl), a
	ld a, $40
	out (VDPControl), a
	;; 2- Output the tile data
	ld hl, ImageData
	ld bc, ImageDataEnd - ImageData
	WriteTilesLoop:
		ld a, (hl)
		out (VDPData), a
		inc hl
		dec bc
		ld a, b
		or c
		jp nz, WriteTilesLoop
	
	;;----------------------------------------------------------
	;; TILES MAP
	;;----------------------------------------------------------
	
	ld a, $00
	out (VDPControl), a
	ld a, $38|$40
	out (VDPControl), a
	
	ld hl, Image
	ld bc, ImageEnd - Image
	WriteMessageLoop:
		ld a, (hl)
		out (VDPData), a
		inc hl
		dec bc
		ld a, b
		or c
		jp nz, WriteMessageLoop
	
	
	;; Turn the screen on
	ld a, %01000000
	out (VDPControl), a
	ld a, $81
	out ($bf), a
	
	;; Infinite loop
	Loop: jp Loop
	
	


